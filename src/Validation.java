public class Validation {
    public String validate(String data) {
        data = data.trim();
        data = data.toUpperCase();
        return data;
    }

    public String validateTrim(String data) {
        data = data.trim();
        return data;
    }

    public String validateUsername(String data) {
        data = data.trim();
        return data;
    }

    public String validatePassword(String data) {
        data = data.trim();
        return data;
    }
}
